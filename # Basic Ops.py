# Basic Math Ops

a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))

print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Multiplication
print(a / b)  # Division
print(a // b) # Floor Division
print(a % b)  # Modulus
print(a ** b) # Exponentiation
