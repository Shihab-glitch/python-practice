# Problem 06 - Print Name of Weekday

input = int(input("Enter number (1-7) : "))

if (input == 7) :
    print("Saturday")

elif (input == 1) :
    print("Sunday")

elif(input == 2) :
    print("Monday")

elif(input == 3) :
    print("Tuesday")

elif(input == 4) :
    print("Wednesday")

elif(input == 5) :
    print("Thursday")

elif (input == 6) :
    print("Friday")

else :
    print("Please, enter number between (1-7)")